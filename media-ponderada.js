function mediaPonderada(arrayConPesos)
{
    let totalValorXPeso = 0; 
    let totalPeso = 0;   

    arrayConPesos.forEach
    (elemento =>
        {           
            totalValorXPeso = elemento[0] * elemento[1] + totalValorXPeso   
            totalPeso = elemento[1] + totalPeso;
        }
        
    );
    return totalValorXPeso / totalPeso;
} 
